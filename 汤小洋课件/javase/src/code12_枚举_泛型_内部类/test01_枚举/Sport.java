package code12_枚举_泛型_内部类.test01_枚举;

public class Sport {

	private String name; // 运动项目名称
	private Season season; // 季节，枚举类型

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Season getSeason() {
		return season;
	}

	public void setSeason(Season season) {
		this.season = season;
	}

}
